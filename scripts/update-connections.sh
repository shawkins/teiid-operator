#!/bin/bash

# taken from https://stackoverflow.com/questions/5014632/how-can-i-parse-a-yaml-file-from-a-linux-shell-script
function parse_yaml {
   local prefix=$2
   local s='[[:space:]]*' w='[a-zA-Z0-9_]*' fs=$(echo @|tr @ '\034')
   sed -ne "s|^\($s\):|\1|" \
        -e "s|^\($s\)\($w\)$s:$s[\"']\(.*\)[\"']$s\$|\1$fs\2$fs\3|p" \
        -e "s|^\($s\)\($w\)$s:$s\(.*\)$s\$|\1$fs\2$fs\3|p"  $1 |
   awk -F$fs '{
      indent = length($1)/2;
      vname[indent] = $2;
      for (i in vname) {if (i > indent) {delete vname[i]}}
      if (length($3) > 0) {
         vn=""; for (i=0; i<indent; i++) {vn=(vn)(vname[i])("_")}
         printf("%s%s%s=%s\n", "'$prefix'",vn, $2, $3);
      }
   }'
}

# get the connection-factories.json file
eval $(parse_yaml ./build/conf/config.yaml)
mvn -s ./scripts/connections/settings.xml org.apache.maven.plugins:maven-dependency-plugin:3.1.2:copy -Dartifact=org.teiid:spring-data-util:${teiidSpringBootVersion}:json:connection_factories -DoutputDirectory=build/conf -U
mv build/conf/spring-data-util-*-connection_factories.json build/conf/connection_factories.json
